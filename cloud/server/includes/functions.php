}

function create_salt()
{
	$salt_hash = hash('sha512',uniqid(rand(), true));
	return $salt_hash;
}

function pass_hash($passwd='')
{
	$result = hash('sha512',$passwd);
	return $result;
}

function get_file_extension($str) 
{
	$i = strrpos($str,".");
	if (!$i)
	{
		return "";
	}
	$l = strlen($str) - $i;
	$ext = substr($str,$i+1,$l);
	return $ext;
}

